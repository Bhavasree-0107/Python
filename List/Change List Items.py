#Change Item Value

list = ["apple", "banana", "cherry"]
list[1] = "blackcurrant"
print(list)

#Change a Range of Item Values

list = ["apple", "banana", "cherry", "orange", "kiwi", "mango"]
list[1:3] = ["blackcurrant", "watermelon"]
print(list)

#Insert Items

list = ["apple", "banana", "cherry"]
list.insert(2, "watermelon")
print(list)